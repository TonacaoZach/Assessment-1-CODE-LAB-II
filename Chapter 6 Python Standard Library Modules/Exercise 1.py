import math

# Find the ceil of x
x1 = 2.3
ceil_result = math.ceil(x1)
print(f"Ceil of {x1} is: {ceil_result}")

# Find the floor of y
x2 = 2.3
floor_result = math.floor(x2)
print(f"Floor of {x2} is: {floor_result}")

# Find the factorial of z
x3 = 5
factorial_result = math.factorial(x3)
print(f"Factorial of {x3} is: {factorial_result}")

# Find the value of base^exponent
base = 2
exponent = 3
power_result = math.pow(base, exponent)
print(f"{base}^{exponent} is: {power_result}")

# Find the square root of w
w = 16
sqrt_result = math.sqrt(w)
print(f"Square root of {w} is: {sqrt_result}")
